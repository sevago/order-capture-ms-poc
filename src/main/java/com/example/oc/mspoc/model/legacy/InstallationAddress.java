package com.example.oc.mspoc.model.legacy;

public class InstallationAddress {
	
	private String coID;
	private String city;
	private String state;
	private String fmsAddressID;
	private String streetName;
	private String postcode;
	private String streetNumber;
	
	
	public String getCoID() {
		return coID;
	}
	public void setCoID(String coID) {
		this.coID = coID;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getFmsAddressID() {
		return fmsAddressID;
	}
	public void setFmsAddressID(String fmsAddressID) {
		this.fmsAddressID = fmsAddressID;
	}
	public String getStreetName() {
		return streetName;
	}
	public void setStreetName(String streetName) {
		this.streetName = streetName;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}
	public String getStreetNumber() {
		return streetNumber;
	}
	public void setStreetNumber(String streetNumber) {
		this.streetNumber = streetNumber;
	}
	
}

